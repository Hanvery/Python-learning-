def matrix_multiply(a, b):
    """ The :py:func:`matrix_algebra.matrix_multiply` function computes
    the product of two matrices.

    Args:
        a (numpy.ndarray): first matrix
        b (numpy.ndarray): second matrix

    Returns:
        (numpy.ndarray): product of a and b

    """
    return a @ b